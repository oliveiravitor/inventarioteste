    private void jButtonAtualizarLocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarLocalActionPerformed
        if (this.jtfIdentificacaoLocal.getText().startsWith(" ") || this.jtfIdentificacaoLocal.getText().equals("")
                && this.jtfSetorLocal.getText().startsWith(" ") || this.jtfSetorLocal.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Favor preencher a identificação e "
                    + "o setor referentes à localidade.");

        } else if (this.jtfLogradouroLocal.getText().startsWith(" ") || this.jtfLogradouroLocal.getText().equals("")
                && this.jtfNumEndLocal.getText().startsWith(" ") || this.jtfNumEndLocal.getText().equals("")
                && this.jtfBairroEndLocal.getText().startsWith(" ") || this.jtfBairroEndLocal.getText().equals("")
                && this.jtfCidadeEndLocal.getText().startsWith(" ") || this.jtfCidadeEndLocal.getText().equals("")
                && this.jtfCepEndLocal.getText().startsWith(" ") || this.jtfCepEndLocal.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Favor preencher os campos obrigatórios"
                    + " do endereço");

        } else {

            Endereco endereco = new Endereco.Builder()
                    .idEndereco(this.localidade.getEnderecoLocalidade().getIdEndereco())
                    .logradouroEndereco(this.jtfLogradouroLocal.getText())
                    .numEndereco(this.jtfNumEndLocal.getText())
                    .bairroEndereco(this.jtfBairroEndLocal.getText())
                    .cidadeEndereco(this.jtfCidadeEndLocal.getText())
                    .cepEndereco(this.jtfCepEndLocal.getText())
                    .statusEndereco('A')
                    .construir();

            if (this.jtfCompEndLocal.getText() != null) {
                endereco.setComplementoEndereco(this.jtfCompEndLocal.getText());
            }

            String estado = this.jcbEstadoEndLocal.getSelectedItem().toString();
            endereco.setEstadoEndereco(estado);

            try {

                boolean atualizarEndereco = ControleSistema
                        .enderecoControle()
                        .atualizarEndereco(endereco);

                int idLocalidade = this.localidade.getIdLocalidade();
                if (atualizarEndereco) {

                    char statusLocal;
                    switch (this.jcbStatusLocal.getSelectedIndex()) {
                        case 0:
                            statusLocal = 'A';
                            break;
                        case 1:
                            statusLocal = 'I';
                            break;
                        default:
                            statusLocal = 'A';
                    }

                    this.localidade = new Localidade.Builder()
                            .idLocalidade(idLocalidade)
                            .enderecoLocalidade(endereco)
                            .identificacaoLocal(this.jtfIdentificacaoLocal.getText())
                            .setorLocal(this.jtfSetorLocal.getText())
                            .statusLocalidade(statusLocal)
                            .construir();

                    boolean atualizarLocalidade = ControleSistema
                            .localidadeControle()
                            .atualizarLocalidade(localidade);

                    if (atualizarLocalidade) {
                        JOptionPane.showMessageDialog(this, "Localidade"
                                + " atualizada com sucesso!");

                        this.dispose();
                    }
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButtonAtualizarLocalActionPerformed

