    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.setTitle("Atualizar Localidade");
        this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        this.setLocationRelativeTo(null);
        
        this.jtfIdentificacaoLocal.setText(this.localidade.getIdentificacaoLocal().trim());
        this.jtfSetorLocal.setText(this.localidade.getSetorLocal().trim());

        switch (localidade.getStatusLocalidade()) {
            case 'A':
                this.jcbStatusLocal.setSelectedIndex(0);
                break;
            case 'I':
                this.jcbStatusLocal.setSelectedIndex(1);
                break;
            default:
                throw new AssertionError();
        }

        this.jtfLogradouroLocal.setText(this.localidade.getEnderecoLocalidade().getLogradouroEndereco().trim());
        this.jtfNumEndLocal.setText(this.localidade.getEnderecoLocalidade().getNumEndereco().trim());

        if (this.localidade.getEnderecoLocalidade().getComplementoEndereco() != null) {
            this.jtfCompEndLocal.setText(this.localidade.getEnderecoLocalidade().getComplementoEndereco().trim());
        } else {
            this.jtfCompEndLocal.setText("");
        }

        this.jtfBairroEndLocal.setText(this.localidade.getEnderecoLocalidade().getBairroEndereco().trim());
        this.jtfCidadeEndLocal.setText(this.localidade.getEnderecoLocalidade().getCidadeEndereco().trim());
        this.jtfCepEndLocal.setText(this.localidade.getEnderecoLocalidade().getCepEndereco().trim());
        this.jcbEstadoEndLocal.setSelectedItem(this.localidade.getEnderecoLocalidade().getEstadoEndereco().trim());
    }//GEN-LAST:event_formWindowOpened

