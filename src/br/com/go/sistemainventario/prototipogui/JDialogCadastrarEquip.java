    private void preencherJComboBoxFornecedor() {
        DefaultComboBoxModel<String> comboBoxFornecedor
                = (DefaultComboBoxModel<String>) jComboBoxFornecedor.getModel();

        ArrayList<Fornecedor> listaFornecedor = null;

        try {
            listaFornecedor = ControleSistema
                    .fornecedorControle()
                    .listarFornecedor();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

        if (listaFornecedor != null) {

            for (Fornecedor fornecedor : listaFornecedor) {
                comboBoxFornecedor.addElement(fornecedor.getIdFornecedor()
                        + "-" + fornecedor.getNomeFornecedor());
            }
        }

        jComboBoxFornecedor.setModel(comboBoxFornecedor);
    }

