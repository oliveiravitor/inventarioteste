    private void jButtonCadastrarEquipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarEquipActionPerformed
        if (isCamposPreenchidos()) {            
            char tipoEquip = this.jComboBoxTipoEquip.getSelectedItem().toString().charAt(0);
            char origemEquip = this.jComboBoxOrigemEquip.getSelectedItem().toString().charAt(0);

            int idFornecedor = getIdFornecedor();

            Equipamento equipamento = new Equipamento.Builder()
                    .tipoEquip(tipoEquip)
                    .origemEquip(origemEquip)
                    .modeloEquip(jtfModeloEquip.getText())
                    .numSerieEquip(jtfNumSerie.getText())
                    .numSerieGabineteEquip(jtfNumSerieGab.getText() + "")
                    .numSerieMouseEquip(jtfNumSerieMouse.getText() + "")
                    .numSerieTecladoEquip(jtfNumSerieTeclado.getText() + "")
                    .numSerieMonitorEquip(jtfNumSerieMonitor.getText() + "")
                    .fornecedor(new Fornecedor.Builder()
                            .idFornecedor(idFornecedor)
                            .construir()
                    )
                    .usarioFinal(new UsuarioFinal.Builder()
                            .idUsuarioFinal(1)
                            .construir()
                    )
                    .localidade(new Localidade.Builder()
                            .idLocalidade(1)
                            .construir()
                    )
                    .construir();
            try {
                
                boolean cadastrarEquipamento = ControleSistema
                        .equipamentoControle()
                        .cadastrarEquipamento(equipamento);
                
                if (cadastrarEquipamento) {
                    JOptionPane.showMessageDialog(this, "Equipamento cadastrado com sucesso!");
                    this.dispose();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }

        } else {
            JOptionPane.showMessageDialog(this, "Favor preencher todos os campos necessários.");
        }
    }//GEN-LAST:event_jButtonCadastrarEquipActionPerformed

