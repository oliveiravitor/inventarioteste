    private boolean isCamposPreenchidos() {
        int selectedIndex = this.jComboBoxTipoEquip.getSelectedIndex();

        switch (selectedIndex) {
            case 0:
                return !this.jtfModeloEquip.getText().trim().equals("")
                        && !this.jtfNumSerie.getText().trim().equals("")
                        && !this.jtfNumSerieGab.getText().trim().equals("")
                        && !this.jtfNumSerieMouse.getText().trim().equals("")
                        && !this.jtfNumSerieTeclado.getText().trim().equals("")
                        && !this.jtfNumSerieMonitor.getText().trim().equals("");
            case 1:
                return !this.jtfModeloEquip.getText().trim().equals("")
                        && !this.jtfNumSerie.getText().trim().equals("");
            case 2:
                return !this.jtfModeloEquip.getText().trim().equals("")
                        && !this.jtfNumSerie.getText().trim().equals("")
                        && !this.jtfNumSerieMouse.getText().trim().equals("")
                        && !this.jtfNumSerieTeclado.getText().trim().equals("");
            case 3:
                return !this.jtfModeloEquip.getText().trim().equals("")
                        && !this.jtfNumSerie.getText().trim().equals("");
        }

        return false;
    }

    private int getIdFornecedor() {
        String selectedItem = this.jComboBoxFornecedor.getSelectedItem().toString();

        String[] split = selectedItem.split("-");

        return Integer.parseInt(split[0]);
    }
