    private void jTableEquipEntradaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEquipEntradaMouseReleased
        /* ao clicar com o mouse sobre uma linha da tabela de equipamentos da aba Entrada Equipamento
        *  este método se encarregará de preencher os dados do fornecedor, usuario final e localidade
        *  vinculados ao equipamento selecionado na tabela
         */
        int selectedRow = this.jTableEquipEntrada.getSelectedRow();
        
        if (selectedRow > - 1) {
            int idUsuarioFinal = (int) this.jTableEquipEntrada.getValueAt(selectedRow, 0);
            
            try {
                UsuarioFinal usuarioFinal = ControleSistema
                        .usuarioControle().buscarUsuarioFinal(idUsuarioFinal);
                
                if (usuarioFinal != null) {
                    this.jtfMatriculaUsuarioEquipIn.setText(String.valueOf(usuarioFinal.getMatriculaUsuario()));
                    this.jtfNomeUsuarioEquipIn.setText(usuarioFinal.getNomeCompletoUsuario());
                    this.jtfEmailUsuarioEquipIn.setText(usuarioFinal.getEmailUsuario());
                    this.jtfFoneUsuarioEquipIn.setText(usuarioFinal.getTelefoneUsuario());
                    
                    String statusUsuarioFinal = "";
                    
                    switch (usuarioFinal.getStatusUsuario()) {
                        case 'A':
                            statusUsuarioFinal = "Ativo";
                            break;
                        case 'I':
                            statusUsuarioFinal = "Inativo";
                            break;
                    }
                    
                    this.jtfStatusUsuarioEquipIn.setText(statusUsuarioFinal);
                }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            
            int idFornecedor = (int) this.jTableEquipEntrada.getValueAt(selectedRow, 1);
            
            try {
                Fornecedor fornecedor = ControleSistema
                        .fornecedorControle().buscarFornecedor(idFornecedor);
                
                if (fornecedor != null) {
                    this.jtfNomeFornecedorEquipIn.setText(fornecedor.getNomeFornecedor());
                    this.jtfContatoFornecedorEquipIn.setText(fornecedor.getNomeContatoFornecedor());
                    this.jtfFoneFornecedorEquipIn.setText(fornecedor.getTelefoneFornecedor());
                    this.jtfEmailFornecedorEquipIn.setText(fornecedor.getEmailFornecedor());
                    
                    String statusFornecedor = "";
                    
                    switch (fornecedor.getStatusFornecedor()) {
                        case 'A':
                            statusFornecedor = "Ativo";
                            break;
                        case 'I':
                            statusFornecedor = "Inativo";
                            break;
                    }
                    
                    this.jtfStatusFornecedorEquipIn.setText(statusFornecedor);
                }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            
            int idLocalidade = (int) this.jTableEquipEntrada.getValueAt(selectedRow, 2);
            
            try {
                Localidade localidade = ControleSistema
                        .localidadeControle().buscarLocalidade(idLocalidade);
                
                if (localidade != null) {
                    this.jtfIdentificacaoLocalEquipIn.setText(localidade.getIdentificacaoLocal());
                    this.jtfSetorLocalEquipIn.setText(localidade.getSetorLocal());
                    
                    String statusLocal = "";
                    
                    switch (localidade.getStatusLocalidade()) {
                        case 'A':
                            statusLocal = "Ativo";
                            break;
                        case 'I':
                            statusLocal = "Inativo";
                            break;
                    }
                    
                    this.jtfStatusLocalEquipIn.setText(statusLocal);
                }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            
            int idEquipamento = (int) this.jTableEquipEntrada.getValueAt(selectedRow, 3);
            
            try {
                this.jtfIpEquipIn.setText("");
                this.jtfHostEquipIn.setText("");
                
                RedeConfig redeConfig = ControleSistema
                        .redeConfig().buscarRedeConfig(idEquipamento);
                
                if (redeConfig != null) {
                    this.jtfIpEquipIn.setText(redeConfig.getIpRedeConfig());
                    this.jtfHostEquipIn.setText(redeConfig.getHostRedeConfig());
                }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            
        }
    }//GEN-LAST:event_jTableEquipEntradaMouseReleased

