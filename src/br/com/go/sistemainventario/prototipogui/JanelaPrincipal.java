    private void preencherTabelaEquipEntrada(char tipoEquip) {
        ArrayList<Equipamento> listaEquipamento = null;
        
        try {
            listaEquipamento = ControleSistema.equipamentoControle().listarEquipamento();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        if (listaEquipamento != null) {
            DefaultTableModel tabelaEquipEntrada = (DefaultTableModel) jTableEquipEntrada.getModel();
            tabelaEquipEntrada.setNumRows(0);
            
            for (int i = 0; i < listaEquipamento.size(); i++) {
                if (listaEquipamento.get(i).getTipoEquip() == tipoEquip || tipoEquip == 'T') {
                    Equipamento equipamento = listaEquipamento.get(i);
                    
                    String tipoEquipamento = "";
                    
                    switch (equipamento.getTipoEquip()) {
                        case 'D':
                            tipoEquipamento = "DESKTOP";
                            break;
                        case 'N':
                            tipoEquipamento = "NOTEBOOK";
                            break;
                        case 'A':
                            tipoEquipamento = "ALL-IN-ONE";
                            break;
                        case 'I':
                            tipoEquipamento = "IMPRESSORA";
                            break;
                    }
                    
                    String origemEquipamento = "";
                    
                    switch (equipamento.getOrigemEquip()) {
                        case 'L':
                            origemEquipamento = "Locação";
                            break;
                        case 'P':
                            origemEquipamento = "Patrimônio";
                            break;
                    }
                    
                    String statusEquip = "";
                    
                    switch (equipamento.getStatusEquip()) {
                        case 'L':
                            statusEquip = "Disponível";
                            break;
                        case 'U':
                            statusEquip = "Alocado";
                            break;
                        case 'M':
                            statusEquip = "Manutenção";
                            break;
                        case 'D':
                            statusEquip = "Descartado";
                            break;
                    }
                    
                    tabelaEquipEntrada.addRow(new Object[]{
                        equipamento.getUsuarioFinal().getIdUsuarioFinal(),
                        equipamento.getFornecedor().getIdFornecedor(),
                        equipamento.getLocalidade().getIdLocalidade(),
                        equipamento.getIdEquip(),
                        tipoEquipamento,
                        origemEquipamento,
                        equipamento.getModeloEquip(),
                        equipamento.getNumSerieEquip(),
                        equipamento.getNumSerieGabineteEquip(),
                        equipamento.getNumSerieMouseEquip(),
                        equipamento.getNumSerieTecladoEquip(),
                        equipamento.getNumSerieMonitorEquip(),
                        statusEquip,
                        equipamento.getDataEntrada()
                    });
                }
            }
            
            jTableEquipEntrada.setModel(tabelaEquipEntrada);
            jTableEquipEntrada.setColumnSelectionAllowed(false);
            jTableEquipEntrada.setRowSelectionAllowed(true);
        }
        
    }
    
