    private void preencherCamposFornecedor() {
        this.jtfNomeFornecedor.setText(this.fornecedor.getNomeFornecedor());
        this.jtfContatoFornecedor.setText(this.fornecedor.getNomeContatoFornecedor());
        this.jtfEmailFornecedor.setText(this.fornecedor.getEmailFornecedor());
        this.jtfFoneFornecedor.setText(this.fornecedor.getTelefoneFornecedor());

        char statusFornecedor = this.fornecedor.getStatusFornecedor();

        switch (statusFornecedor) {
            case 'A':
                this.jcbStatusFornecedor.setSelectedIndex(0);
                break;
            case 'I':
                this.jcbStatusFornecedor.setSelectedIndex(1);
                break;
        }

        this.jtfLogradouroEndFornecedor.setText(
                this.fornecedor.getEnderecoFornecedor().getLogradouroEndereco());

        this.jtfNumEndFornecedor.setText(
                this.fornecedor.getEnderecoFornecedor().getNumEndereco());

        if (this.fornecedor.getEnderecoFornecedor().getComplementoEndereco() != null) {
            this.jtfCompEndFornecedor.setText(
                    this.fornecedor.getEnderecoFornecedor().getComplementoEndereco());
        }

        this.jtfBairroEndFornecedor.setText(
                this.fornecedor.getEnderecoFornecedor().getBairroEndereco());

        this.jtfCidadeFornecedor.setText(
                this.fornecedor.getEnderecoFornecedor().getCidadeEndereco());

        this.jtfCepEndFornecedor.setText(
                fornecedor.getEnderecoFornecedor().getCepEndereco());

        this.jcbEstadoEndFornecedor.setSelectedItem(
                this.fornecedor.getEnderecoFornecedor().getEstadoEndereco());
    }
