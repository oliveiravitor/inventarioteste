    private void jButtonAtualizarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarFornecedorActionPerformed
        if (jtfNomeFornecedor.getText().startsWith(" ") || jtfNomeFornecedor.getText().equals("")
                && jtfContatoFornecedor.getText().startsWith(" ") || jtfContatoFornecedor.getText().equals("")
                && jtfEmailFornecedor.getText().startsWith(" ") || jtfEmailFornecedor.getText().equals("")
                && jtfFoneFornecedor.getText().startsWith(" ") || jtfFoneFornecedor.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Favor preencher, nome, "
                    + "contato, email e telefone do Fornecedor");

        } else if (jtfLogradouroEndFornecedor.getText().startsWith(" ") || jtfLogradouroEndFornecedor.getText().equals("")
                && jtfNumEndFornecedor.getText().startsWith(" ") || jtfNumEndFornecedor.getText().equals("")
                && jtfBairroEndFornecedor.getText().startsWith(" ") || jtfBairroEndFornecedor.getText().equals("")
                && jtfCidadeFornecedor.getText().startsWith(" ") || jtfCidadeFornecedor.getText().equals("")
                && jtfCepEndFornecedor.getText().startsWith(" ") || jtfCepEndFornecedor.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Favor preencher os campos obrigatórios"
                    + " do endereço");

        } else {
            Endereco endereco = new Endereco.Builder()
                    .idEndereco(this.fornecedor.getEnderecoFornecedor().getIdEndereco())
                    .logradouroEndereco(jtfLogradouroEndFornecedor.getText())
                    .numEndereco(jtfNumEndFornecedor.getText())
                    .bairroEndereco(jtfBairroEndFornecedor.getText())
                    .cidadeEndereco(jtfCidadeFornecedor.getText())
                    .cepEndereco(jtfCepEndFornecedor.getText())
                    .statusEndereco('A')
                    .construir();

            if (jtfCompEndFornecedor.getText() != null) {
                endereco.setComplementoEndereco(jtfCompEndFornecedor.getText());
            }

            String estado = jcbEstadoEndFornecedor.getSelectedItem().toString();
            endereco.setEstadoEndereco(estado);

            try {
                boolean atualizarEndereco = ControleSistema
                        .enderecoControle()
                        .atualizarEndereco(endereco);

                if (atualizarEndereco) {
                    char statusFornecedor = 'A';

                    switch (jcbStatusFornecedor.getSelectedItem().toString()) {
                        case "Ativo":
                            statusFornecedor = 'A';
                            break;
                        case "Inativo":
                            statusFornecedor = 'I';
                            break;
                    }

                    int idFornecedor = this.fornecedor.getIdFornecedor();
                    this.fornecedor = new Fornecedor.Builder()
                            .idFornecedor(idFornecedor)
                            .enderecoFornecedor(endereco)
                            .nomeFornecedor(jtfNomeFornecedor.getText())
                            .nomeContatoFornecedor(jtfContatoFornecedor.getText())
                            .emailFornecedor(jtfEmailFornecedor.getText())
                            .telefoneFornecedor(jtfFoneFornecedor.getText())
                            .statusFornecedor(statusFornecedor)
                            .construir();

                    boolean atualizarFornecedor = ControleSistema
                            .fornecedorControle()
                            .atualizarFornecedor(fornecedor);

                    if (atualizarFornecedor) {
                        JOptionPane.showMessageDialog(this,
                                "Fornecedor atualizado com sucesso!");
                        this.dispose();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Falha ao atualizar o endereço do Fornecedor."
                            + "\nFavor entrar em contato com o suporte.");
                    this.dispose();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }

            this.dispose();
        }
    }//GEN-LAST:event_jButtonAtualizarFornecedorActionPerformed

